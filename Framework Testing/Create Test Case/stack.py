class Stack:
     def __init__(self):
         self.a = 0
         self.b = 0
         self.c = 0

     def zarb(self,a,b,c):
         return a*b*c

     def jam(self,a,b,c):
         return a+b+c

     def menha(self,a,b,c):
         return a-b-c

     def peek(self,a,b,c):
         return a+b-c
